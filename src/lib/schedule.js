export function groupByMonth(schedule) {
  const groups = {};
  schedule.forEach(d => {
    const key = new Date(d.date + 'T00:00:00').toLocaleDateString('en-IN',{ month:'short', year:'numeric' });
    if (!groups[key]) groups[key] = [];
    groups[key].push(d);
  });
  return groups;
}
